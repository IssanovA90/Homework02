let num = +prompt('напиши число от 0-9')

if(num == 1 ){
    alert('1-!')
}
else if(num == 2 ){
    alert('2-@')
}
else if(num == 3 ){
    alert('3-#')
}
else if(num == 4 ){
    alert('4-$')
}
else if(num == 5 ){
    alert('5-%')
}
else if(num == 6 ){
    alert('6-^')
}
else if(num == 7 ){
    alert('7-&')
}
else if(num == 8 ){
    alert('8-*')
}
else if(num == 9 ){
    alert('9-(')
}
else{
    alert()
}
